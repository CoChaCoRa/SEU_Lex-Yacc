int *read_tokens(string filename)
{
    int countToken=0;
    ifstream in(filename);
    while (!in.eof())
    {
        string temp;
        in >> temp;
        if(temp=="SINGLE_LINE_COMMENT"||temp=="MULTI_LINE_COMMENT")
              continue;
        countToken++;
    }
    int *tokens = new int[countToken+1];
    ifstream in1(filename);
    int i = 0;
    while (!in1.eof())
    {
        string temp;
        in1 >> temp;
        if(temp=="SINGLE_LINE_COMMENT"||temp=="MULTI_LINE_COMMENT")
            continue;
        for(int j=0;j<sizeof(tokensMapNum)/sizeof(int);j++)
        {
            if(temp==tokensMapStr[j])
            {
                  tokens[i]=tokensMapNum[j];
                  break;
            }
        }
        i++;
    }
    tokens[countToken]=TERMINATED_SYMBOL+1;
    return tokens;
}
